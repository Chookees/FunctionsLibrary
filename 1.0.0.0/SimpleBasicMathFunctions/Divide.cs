namespace SimpleBasicMathFunctions
{
    public class Divide
    {
        /// <summary>
        /// Division for Double type
        /// </summary>
        /// <param name="num1"></param>
        /// <param name="num2"></param>
        /// <returns></returns>
        public static double CalculateDouble(double num1, double num2)
        {
            return num1 / num2;
        }

        /// <summary>
        /// Division for Int type (Int16)
        /// </summary>
        /// <param name="num1"></param>
        /// <param name="num2"></param>
        /// <returns></returns>
        public static int CalculateInt(int num1, int num2)
        {
            return num1 / num2;
        }

        /// <summary>
        /// Division for Float type
        /// </summary>
        /// <param name="num1"></param>
        /// <param name="num2"></param>
        /// <returns></returns>
        public static float CalculateFloat(float num1, float num2)
        {
            return num1 / num2;
        }

        /// <summary>
        /// Division for Long type (Int64)
        /// </summary>
        /// <param name="num1"></param>
        /// <param name="num2"></param>
        /// <returns></returns>
        public static long CalculateLong(long num1, long num2)
        {
            return num1 / num2;
        }
    }
}
